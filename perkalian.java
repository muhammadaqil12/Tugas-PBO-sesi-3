public class perkalian {


    
    public static void main(String[] argume){
        
                int num = 4; 
                int limit = 4; // Batas perkalian
                
                
                for (int i = 1; i <= limit; i++) {
                    int perkalian = num * i;
                    System.out.println(num + " * " + i + " = " + perkalian);
                }
    
                int base = 99; // Angka yang akan dikalikan
                

                for (int i = 20; i <= 24; i++) {
                    int kali = base * i;

                    System.out.println (base + " * " + i + " = " + kali);

                }
    }
}    
            


        
        
